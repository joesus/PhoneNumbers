import XCTest
@testable import PhoneNumbers

class BasicPhoneNumberTests: XCTestCase {

    func testCreatingPhoneNumberWithValidInput() {
        ValidUSTelephoneNumbers.forEach { rawString in
            guard let phoneNumber = BasicPhoneNumber(rawValue: rawString) else {
                return XCTFail("\(rawString) should create a valid phone number")
            }

            XCTAssertEqual(phoneNumber.rawValue, rawString,
                           "The raw value \(rawString) should be stored unmodified")
        }
    }

    func testCreatingPhoneNumberWithInvalidInput() {
        InvalidUSTelephoneNumbers.forEach { phoneNumber in
            XCTAssertNil(BasicPhoneNumber(rawValue: phoneNumber),
                         "\(phoneNumber) should not create a valid phone number")
        }
    }
}

